package match;

public class CompanyBean {
	private int company_idx;
	private String company_name;
	private String company_type;
	private int company_rnum;
	private String company_head;
	private String company_address;
	
	public int getCompany_idx() {
		return company_idx;
	}
	public void setCompany_idx(int company_idx) {
		this.company_idx = company_idx;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_type() {
		return company_type;
	}
	public void setCompany_type(String company_type) {
		this.company_type = company_type;
	}
	public int getCompany_rnum() {
		return company_rnum;
	}
	public void setCompany_rnum(int company_rnum) {
		this.company_rnum = company_rnum;
	}
	public String getCompany_head() {
		return company_head;
	}
	public void setCompany_head(String company_head) {
		this.company_head = company_head;
	}
	public String getCompany_address() {
		return company_address;
	}
	public void setCompany_address(String company_address) {
		this.company_address = company_address;
	}
}