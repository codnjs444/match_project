package match.resume;

public class InternshipBean {
	private int resume_idx;
	private String internship_type;
	private String internship_cname;
	private String internship_syear;
	private String internship_eyear;
	private String internship_duty;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getInternship_type() {
		return internship_type;
	}
	public void setInternship_type(String internship_type) {
		this.internship_type = internship_type;
	}
	public String getInternship_cname() {
		return internship_cname;
	}
	public void setInternship_cname(String internship_cname) {
		this.internship_cname = internship_cname;
	}
	public String getInternship_syear() {
		return internship_syear;
	}
	public void setInternship_syear(String internship_syear) {
		this.internship_syear = internship_syear;
	}
	public String getInternship_eyear() {
		return internship_eyear;
	}
	public void setInternship_eyear(String internship_eyear) {
		this.internship_eyear = internship_eyear;
	}
	public String getInternship_duty() {
		return internship_duty;
	}
	public void setInternship_duty(String internship_duty) {
		this.internship_duty = internship_duty;
	}
}