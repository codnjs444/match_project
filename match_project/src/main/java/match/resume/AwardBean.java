package match.resume;

public class AwardBean {
	private int resume_idx;
	private String award_name;
	private String award_cname;
	private String award_syear;
	private String award_content;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getAward_name() {
		return award_name;
	}
	public void setAward_name(String award_name) {
		this.award_name = award_name;
	}
	public String getAward_cname() {
		return award_cname;
	}
	public void setAward_cname(String award_cname) {
		this.award_cname = award_cname;
	}
	public String getAward_syear() {
		return award_syear;
	}
	public void setAward_syear(String award_syear) {
		this.award_syear = award_syear;
	}
	public String getAward_content() {
		return award_content;
	}
	public void setAward_content(String award_content) {
		this.award_content = award_content;
	}
}