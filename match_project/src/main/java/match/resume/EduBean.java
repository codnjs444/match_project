package match.resume;

public class EduBean {
	private int resume_idx;
	private String edu_type;
	private String edu_school;
	private String edu_major;
	private String edu_syear;
	private String edu_eyear;
	private String edu_status;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getEdu_type() {
		return edu_type;
	}
	public void setEdu_type(String edu_type) {
		this.edu_type = edu_type;
	}
	public String getEdu_school() {
		return edu_school;
	}
	public void setEdu_school(String edu_school) {
		this.edu_school = edu_school;
	}
	public String getEdu_major() {
		return edu_major;
	}
	public void setEdu_major(String edu_major) {
		this.edu_major = edu_major;
	}
	public String getEdu_syear() {
		return edu_syear;
	}
	public void setEdu_syear(String edu_syear) {
		this.edu_syear = edu_syear;
	}
	public String getEdu_eyear() {
		return edu_eyear;
	}
	public void setEdu_eyear(String edu_eyear) {
		this.edu_eyear = edu_eyear;
	}
	public String getEdu_status() {
		return edu_status;
	}
	public void setEdu_status(String edu_status) {
		this.edu_status = edu_status;
	}
}