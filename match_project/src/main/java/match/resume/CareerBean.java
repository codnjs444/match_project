package match.resume;

public class CareerBean {
	private int resume_idx;
	private String career_cname;
	private String career_dep;
	private String career_job;
	private String career_syear;
	private String career_eyear;
	private String career_duty;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getCareer_cname() {
		return career_cname;
	}
	public void setCareer_cname(String career_cname) {
		this.career_cname = career_cname;
	}
	public String getCareer_dep() {
		return career_dep;
	}
	public void setCareer_dep(String career_dep) {
		this.career_dep = career_dep;
	}
	public String getCareer_job() {
		return career_job;
	}
	public void setCareer_job(String career_job) {
		this.career_job = career_job;
	}
	public String getCareer_syear() {
		return career_syear;
	}
	public void setCareer_syear(String career_syear) {
		this.career_syear = career_syear;
	}
	public String getCareer_eyear() {
		return career_eyear;
	}
	public void setCareer_eyear(String career_eyear) {
		this.career_eyear = career_eyear;
	}
	public String getCareer_duty() {
		return career_duty;
	}
	public void setCareer_duty(String career_duty) {
		this.career_duty = career_duty;
	}
}