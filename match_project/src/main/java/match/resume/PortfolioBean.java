package match.resume;

public class PortfolioBean {
	private int resume_idx;
	private String portfolio_name;
	private String portfolio_url;
	private String portfolio_fname;
	private String portfolio_extension;
	private String portfolio_udate;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getPortfolio_name() {
		return portfolio_name;
	}
	public void setPortfolio_name(String portfolio_name) {
		this.portfolio_name = portfolio_name;
	}
	public String getPortfolio_url() {
		return portfolio_url;
	}
	public void setPortfolio_url(String portfolio_url) {
		this.portfolio_url = portfolio_url;
	}
	public String getPortfolio_fname() {
		return portfolio_fname;
	}
	public void setPortfolio_fname(String portfolio_fname) {
		this.portfolio_fname = portfolio_fname;
	}
	public String getPortfolio_extension() {
		return portfolio_extension;
	}
	public void setPortfolio_extension(String portfolio_extension) {
		this.portfolio_extension = portfolio_extension;
	}
	public String getPortfolio_udate() {
		return portfolio_udate;
	}
	public void setPortfolio_udate(String portfolio_udate) {
		this.portfolio_udate = portfolio_udate;
	}
}