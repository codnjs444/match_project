package match.resume;

public class GlobalexBean {
	private int resume_idx;
	private String globalex_name;
	private String globalex_cname;
	private String globalex_syear;
	private String globalex_eyear;
	private String globalex_content;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getGlobalex_name() {
		return globalex_name;
	}
	public void setGlobalex_name(String globalex_name) {
		this.globalex_name = globalex_name;
	}
	public String getGlobalex_cname() {
		return globalex_cname;
	}
	public void setGlobalex_cname(String globalex_cname) {
		this.globalex_cname = globalex_cname;
	}
	public String getGlobalex_syear() {
		return globalex_syear;
	}
	public void setGlobalex_syear(String globalex_syear) {
		this.globalex_syear = globalex_syear;
	}
	public String getGlobalex_eyear() {
		return globalex_eyear;
	}
	public void setGlobalex_eyear(String globalex_eyear) {
		this.globalex_eyear = globalex_eyear;
	}
	public String getGlobalex_content() {
		return globalex_content;
	}
	public void setGlobalex_content(String globalex_content) {
		this.globalex_content = globalex_content;
	}
}