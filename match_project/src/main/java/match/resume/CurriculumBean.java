package match.resume;

public class CurriculumBean {
	private int resume_idx;
	private String curriculum_name;
	private String curriculum_cname;
	private String curriculum_syear;
	private String curriculum_eyear;
	private String curriculum_content;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getCurriculum_name() {
		return curriculum_name;
	}
	public void setCurriculum_name(String curriculum_name) {
		this.curriculum_name = curriculum_name;
	}
	public String getCurriculum_cname() {
		return curriculum_cname;
	}
	public void setCurriculum_cname(String curriculum_cname) {
		this.curriculum_cname = curriculum_cname;
	}
	public String getCurriculum_syear() {
		return curriculum_syear;
	}
	public void setCurriculum_syear(String curriculum_syear) {
		this.curriculum_syear = curriculum_syear;
	}
	public String getCurriculum_eyear() {
		return curriculum_eyear;
	}
	public void setCurriculum_eyear(String curriculum_eyear) {
		this.curriculum_eyear = curriculum_eyear;
	}
	public String getCurriculum_content() {
		return curriculum_content;
	}
	public void setCurriculum_content(String curriculum_content) {
		this.curriculum_content = curriculum_content;
	}
}