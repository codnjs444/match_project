package match.category;

public class skill_categoryBean {
	private String skill_code;
	private String skill_name;
	private String skill_scode;
	private String skill_sname;
	
	public String getSkill_code() {
		return skill_code;
	}
	public void setSkill_code(String skill_code) {
		this.skill_code = skill_code;
	}
	public String getSkill_name() {
		return skill_name;
	}
	public void setSkill_name(String skill_name) {
		this.skill_name = skill_name;
	}
	public String getSkill_scode() {
		return skill_scode;
	}
	public void setSkill_scode(String skill_scode) {
		this.skill_scode = skill_scode;
	}
	public String getSkill_sname() {
		return skill_sname;
	}
	public void setSkill_sname(String skill_sname) {
		this.skill_sname = skill_sname;
	}
	
}
