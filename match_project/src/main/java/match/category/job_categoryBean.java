package match.category;

public class job_categoryBean {
	private int job_code;
	private String job_name;
	private int job_scode;
	private String job_sname;
	
	public int getJob_code() {
		return job_code;
	}
	public void setJob_code(int job_code) {
		this.job_code = job_code;
	}
	public String getJob_name() {
		return job_name;
	}
	public void setJob_name(String job_name) {
		this.job_name = job_name;
	}
	public int getJob_scode() {
		return job_scode;
	}
	public void setJob_scode(int job_scode) {
		this.job_scode = job_scode;
	}
	public String getJob_sname() {
		return job_sname;
	}
	public void setJob_sname(String job_sname) {
		this.job_sname = job_sname;
	}
	
}
