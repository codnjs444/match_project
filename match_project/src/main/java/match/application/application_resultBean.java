package match.application;

public class application_resultBean {	
	private int result_idx;
	private int posting_idx;
	private int procedure_num;
	private String procedure_name;
	private String user_id;
	private String result_status;
	private String result_day;
	
	public int getResult_idx() {
		return result_idx;
	}
	public void setResult_idx(int result_idx) {
		this.result_idx = result_idx;
	}
	public int getPosting_idx() {
		return posting_idx;
	}
	public void setPosting_idx(int posting_idx) {
		this.posting_idx = posting_idx;
	}
	public int getProcedure_num() {
		return procedure_num;
	}
	public void setProcedure_num(int procedure_num) {
		this.procedure_num = procedure_num;
	}
	public String getProcedure_name() {
		return procedure_name;
	}
	public void setProcedure_name(String procedure_name) {
		this.procedure_name = procedure_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getResult_status() {
		return result_status;
	}
	public void setResult_status(String result_status) {
		this.result_status = result_status;
	}
	public String getResult_day() {
		return result_day;
	}
	public void setResult_day(String result_day) {
		this.result_day = result_day;
	}
	
	
}
