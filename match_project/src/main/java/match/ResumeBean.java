package match;

public class ResumeBean {
	private int resume_idx;
	private String user_id;
	private String resume_name;
	private String self_intro1;
	private String self_intro2;
	private String self_intro3;
	
	public int getResume_idx() {
		return resume_idx;
	}
	public void setResume_idx(int resume_idx) {
		this.resume_idx = resume_idx;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getResume_name() {
		return resume_name;
	}
	public void setResume_name(String resume_name) {
		this.resume_name = resume_name;
	}
	public String getSelf_intro1() {
		return self_intro1;
	}
	public void setSelf_intro1(String self_intro1) {
		this.self_intro1 = self_intro1;
	}
	public String getSelf_intro2() {
		return self_intro2;
	}
	public void setSelf_intro2(String self_intro2) {
		this.self_intro2 = self_intro2;
	}
	public String getSelf_intro3() {
		return self_intro3;
	}
	public void setSelf_intro3(String self_intro3) {
		this.self_intro3 = self_intro3;
	}
}