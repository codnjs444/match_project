package match.posting;

public class OpenPositionBean {
	private int posting_idx;
	private String openposition_name;
	private String openposition_duty;
	private int openposition_num;
	private String openposition_sname;
	private String openposition_position;
	public int getPosting_idx() {
		return posting_idx;
	}
	public void setPosting_idx(int posting_idx) {
		this.posting_idx = posting_idx;
	}
	public String getOpenposition_name() {
		return openposition_name;
	}
	public void setOpenposition_name(String openposition_name) {
		this.openposition_name = openposition_name;
	}
	public String getOpenposition_duty() {
		return openposition_duty;
	}
	public void setOpenposition_duty(String openposition_duty) {
		this.openposition_duty = openposition_duty;
	}
	public int getOpenposition_num() {
		return openposition_num;
	}
	public void setOpenposition_num(int openposition_num) {
		this.openposition_num = openposition_num;
	}
	public String getOpenposition_sname() {
		return openposition_sname;
	}
	public void setOpenposition_sname(String openposition_sname) {
		this.openposition_sname = openposition_sname;
	}
	public String getOpenposition_position() {
		return openposition_position;
	}
	public void setOpenposition_position(String openposition_position) {
		this.openposition_position = openposition_position;
	}
	
}
