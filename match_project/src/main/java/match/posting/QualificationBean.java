package match.posting;

public class QualificationBean {
	private int posting_idx;
	private String qualification_edutype;
	private String qualification_gender;
	private String qualification_experience;
	private String qualification_skill;
	private String qualification_certificate;
	
	public int getPosting_idx() {
		return posting_idx;
	}
	public void setPosting_idx(int posting_idx) {
		this.posting_idx = posting_idx;
	}
	public String getQualification_edutype() {
		return qualification_edutype;
	}
	public void setQualification_edutype(String qualification_edutype) {
		this.qualification_edutype = qualification_edutype;
	}
	public String getQualification_gender() {
		return qualification_gender;
	}
	public void setQualification_gender(String qualification_gender) {
		this.qualification_gender = qualification_gender;
	}
	public String getQualification_experience() {
		return qualification_experience;
	}
	public void setQualification_experience(String qualification_experience) {
		this.qualification_experience = qualification_experience;
	}
	public String getQualification_skill() {
		return qualification_skill;
	}
	public void setQualification_skill(String qualification_skill) {
		this.qualification_skill = qualification_skill;
	}
	public String getQualification_certificate() {
		return qualification_certificate;
	}
	public void setQualification_certificate(String qualification_certificate) {
		this.qualification_certificate = qualification_certificate;
	}
	
}
