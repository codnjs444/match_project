package match.posting;

public class procedureBean {
	private int posting_idx;
	private int procedure_num;
	private String procedure_name;
	private String procedure_sdatetime;
	private String procedure_edatetime;
	public int getPosting_idx() {
		return posting_idx;
	}
	public void setPosting_idx(int posting_idx) {
		this.posting_idx = posting_idx;
	}
	public int getProcedure_num() {
		return procedure_num;
	}
	public void setProcedure_num(int procedure_num) {
		this.procedure_num = procedure_num;
	}
	public String getProcedure_name() {
		return procedure_name;
	}
	public void setProcedure_name(String procedure_name) {
		this.procedure_name = procedure_name;
	}
	public String getProcedure_sdatetime() {
		return procedure_sdatetime;
	}
	public void setProcedure_sdatetime(String procedure_sdatetime) {
		this.procedure_sdatetime = procedure_sdatetime;
	}
	public String getProcedure_edatetime() {
		return procedure_edatetime;
	}
	public void setProcedure_edatetime(String procedure_edatetime) {
		this.procedure_edatetime = procedure_edatetime;
	}
	
	
}
