package match;

public class ManagerBean {
	private String manager_id;
	private int company_idx;
	private String manager_name;
	private String manager_pwd;
	private String manager_email;
	private String manager_phonenum;
	
	public String getManager_id() {
		return manager_id;
	}
	public void setManager_id(String manager_id) {
		this.manager_id = manager_id;
	}
	public int getCompany_idx() {
		return company_idx;
	}
	public void setCompany_idx(int company_idx) {
		this.company_idx = company_idx;
	}
	public String getManager_name() {
		return manager_name;
	}
	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}
	public String getManager_pwd() {
		return manager_pwd;
	}
	public void setManager_pwd(String manager_pwd) {
		this.manager_pwd = manager_pwd;
	}
	public String getManager_email() {
		return manager_email;
	}
	public void setManager_email(String manager_email) {
		this.manager_email = manager_email;
	}
	public String getManager_phonenum() {
		return manager_phonenum;
	}
	public void setManager_phonenum(String manager_phonenum) {
		this.manager_phonenum = manager_phonenum;
	}
}